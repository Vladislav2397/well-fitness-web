import { getPaginationArray } from './usePaginationView'

describe('test getPaginationArray', () => {
    it.each([
        [1, ['1', '2', '3', '4', '5', '...', '10']],
        [2, ['1', '2', '3', '4', '5', '...', '10']],
        [3, ['1', '2', '3', '4', '5', '...', '10']],
        [4, ['1', '...', '3', '4', '5', '...', '10']],
        [5, ['1', '...', '4', '5', '6', '...', '10']],
        [6, ['1', '...', '5', '6', '7', '...', '10']],
        [7, ['1', '...', '6', '7', '8', '...', '10']],
        [8, ['1', '...', '6', '7', '8', '9', '10']],
        [9, ['1', '...', '6', '7', '8', '9', '10']],
        [10, ['1', '...', '6', '7', '8', '9', '10']],
    ])('should %s be equal %s value', (page, result) => {
        expect(getPaginationArray(10, page)).toStrictEqual(result)
    })
})
